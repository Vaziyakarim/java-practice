public class unary {
    public static void main(String[] args) {
        //unary
        //increment
        //decrement
        int num=10;
        num += 2;
        System.out.println(num);
        num++;
         System.out.println(num);
        num--;
         System.out.println(num);

        ++num;
         System.out.println(num);
        --num; 
        System.out.println(num);

    }
}
